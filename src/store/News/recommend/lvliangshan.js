在联合国所谓“不适宜人类居住”的地方、山西省贫困程度最深的地区，艰苦的自然环境让这里的人们磨炼出异于平常的豪爽和悍勇。他们倔强的脊背似乎能够承受更大的压力，只要有一束希望之光扫过，便迅即爆发出更大的能量，不顾一切去追逐希望。
