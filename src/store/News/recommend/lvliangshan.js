革命时期，为了风展红旗，刘胡兰们将生死置之度外；建设时期，为了拔掉穷根，梁宝们将“小我”无限压缩，与贫穷这个敌人斗争直至生命的最后一刻。
在山西，名字中带“垣”的地方，多是坐落在黄土高原沟壑纵横的山峦之上的小村庄。梁宝担任村党支部书记的石楼县灵泉镇薛家垣正是这样一个地方。1986年，上海第一座地铁车站动工兴建，而在薛家垣，点灯依旧用油，吃水依旧靠担，出山依旧靠腿。
