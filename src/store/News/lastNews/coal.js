古交，是座怎样的城市？
同样的问题，时间会让答案变得截然不同。
过去，典型的答案是：这是一座“煤城”，依煤而建、因煤而兴。人们印象里，这座资源型城市灰蒙蒙、黑乎乎。
现在，古交人底气十足地回答：这是一座“美城”！绿色新兴产业蓬勃发展，天蓝水清岸绿成为新常态，一个个村庄经过“六乱”整治的洗礼，变得越来越美丽。
截然不同的答案背后，是这座城市对高质量转型发展的深刻理解与生动实践。
